package com.example.forcse2200;

public class For_Events {
    String last_event, event1, event2, event3, event4, event5;
    public For_Events(){}

    public For_Events(String last_event, String event1, String event2, String event3, String event4, String event5) {
        this.last_event = last_event;
        this.event1 = event1;
        this.event2 = event2;
        this.event3 = event3;
        this.event4 = event4;
        this.event5 = event5;
    }

    public String getLast_event() {
        return last_event;
    }

    public void setLast_event(String last_event) {
        this.last_event = last_event;
    }

    public String getEvent1() {
        return event1;
    }

    public void setEvent1(String event1) {
        this.event1 = event1;
    }

    public String getEvent2() {
        return event2;
    }

    public void setEvent2(String event2) {
        this.event2 = event2;
    }

    public String getEvent3() {
        return event3;
    }

    public void setEvent3(String event3) {
        this.event3 = event3;
    }

    public String getEvent4() {
        return event4;
    }

    public void setEvent4(String event4) {
        this.event4 = event4;
    }

    public String getEvent5() {
        return event5;
    }

    public void setEvent5(String event5) {
        this.event5 = event5;
    }
}
